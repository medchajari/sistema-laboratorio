    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       try {
        // Obtener la forma de pago seleccionada
        String formaPagoSeleccionada = (String) cbxFormaPago.getSelectedItem();

        // Obtener las fechas desde los DateChooser
        Date fechaDesde = dcDesde.getDate();
        Date fechaHasta = dcHasta.getDate();

        // Validar que las fechas no sean nulas
        if (fechaDesde == null || fechaHasta == null) {
            JOptionPane.showMessageDialog(this, "Por favor seleccione las fechas", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Variables para almacenar los resultados
        List<Caja> resultados = new ArrayList<>();

        // Buscar los registros de caja seg√∫n la forma de pago y fechas
        resultados = cData.buscarPorFormaDePagoYFechas(formaPagoSeleccionada, fechaDesde, fechaHasta);

        // Limpiar la tabla antes de agregar los nuevos resultados
        DefaultTableModel model = (DefaultTableModel) tableInforme.getModel();
        model.setRowCount(0);

        // Agregar los datos a la tabla
        for (Caja caja : resultados) {
            model.addRow(new Object[]{
                caja.getIdCaja(),
                caja.getUsuario(),
                caja.getMonto(),
                caja.getForma_de_pago(),
                caja.getNumero_protocolo(),
                caja.getFecha(),
                caja.getEfectivo(),
                caja.getTransferencia(),
                caja.getCuenta_corriente(),
                caja.getObra_social()
            });
        }
          // Sumar los montos y mostrar en txtResultado
        double totalMonto = 0;
         double totalEfe = 0;
          double totalTran = 0;
           double totalCC = 0;
            double totalOS = 0;
        for (int i = 0; i < model.getRowCount(); i++) {
            totalMonto += (double) model.getValueAt(i, 2); // Suma los valores de la columna "Monto"
             totalEfe += (double) model.getValueAt(i, 6);
             totalTran += (double) model.getValueAt(i, 7);
             totalCC += (double) model.getValueAt(i, 8);
             totalOS += (double) model.getValueAt(i, 9);
        }
        txtTotal.setText(String.valueOf(totalMonto));
        txtEfectivo.setText(String.valueOf(totalEfe));
        txtTransferencia.setText(String.valueOf(totalTran));
        txtCC.setText(String.valueOf(totalCC));
        txtOS.setText(String.valueOf(totalOS));
        
        

        // Mostrar mensaje si no hay resultados
        if (resultados.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encontraron resultados para la b√∫squeda.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
        }
        

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al realizar la b√∫squeda: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

