    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
     
            try {
         
            
            String nombreyapellido = txtNombre.getText();
            String telefono = txtTelefono.getText();
            String direccion = txtDireccion.getText();
            
            
            

            // Validar que los campos obligatorios no estén vacíos
            if (nombreyapellido.isEmpty() || telefono.isEmpty() || direccion.isEmpty()  ) {
               
                JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
              
                proData.agregarProfesional(nombreyapellido,telefono, direccion);
               
                // Limpia los campos de entrada después de agregar el proveedor (opcional)
                txtNombre.setText("");                
                txtTelefono.setText("");
                txtDireccion.setText("");
               
                  
                   
            }
        } catch (NumberFormatException e) {
           
            JOptionPane.showMessageDialog(this, "Los campos numéricos contienen valores no válidos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
         
            Profesionales proSeleccionada = (Profesionales) cbxProfesional.getSelectedItem();
            if (proSeleccionada == null) {
                JOptionPane.showMessageDialog(this, "Por favor, seleccione una obra social.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int idObraSocial = proSeleccionada.getId_profesional();
            String nombre = txtNombre.getText();
            String telefono = txtTelefono.getText();
            String direccion = txtDireccion.getText();

        
            if (nombre.isEmpty() || telefono.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
               
                proData.actualizarProfesional(idObraSocial, nombre, telefono, direccion);
                 
                txtNombre.setText("");
                txtTelefono.setText("");
                txtDireccion.setText(""); // Recargar el combo box después de modificar
                llenarComboBoxProf();
            }
       
    }//GEN-LAST:event_btnModificarActionPerformed

