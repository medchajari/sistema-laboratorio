private void llenarComboBoxProf() {
    List<Profesionales> prof = proData.listarProfesionales();
    DefaultComboBoxModel<Profesionales> model = new DefaultComboBoxModel<>();

    // Agregar un elemento vacío al inicio
    model.addElement(null);

    for (Profesionales profesionales : prof) {
        model.addElement(profesionales);
    }

    cbxProfesional.setModel(model);

    
    cbxProfesional.setRenderer(new javax.swing.DefaultListCellRenderer() {
        @Override
        public java.awt.Component getListCellRendererComponent(
                javax.swing.JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
            if (value instanceof Profesionales) {
                Profesionales profesionales = (Profesionales) value;
                value = profesionales.getNombre_apellido();
            } else if (value == null) {
                value = ""; 
            }
            return super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
        }
    });
}
  

  private void agregarActionListenerComboBox() {
    cbxProfesional.addActionListener(new java.awt.event.ActionListener() {
        @Override
        public void actionPerformed(java.awt.event.ActionEvent evt) {
            Profesionales profSeleccionado = (Profesionales) cbxProfesional.getSelectedItem();
            if (profSeleccionado != null) {
                txtNombre.setText(profSeleccionado.getNombre_apellido());
                 txtTelefono.setText(profSeleccionado.getTelefono());
                  txtDireccion.setText(profSeleccionado.getDireccion());
            } else {
                txtNombre.setText("");
                txtTelefono.setText("");
                txtDireccion.setText("");
            }
        }
    });
}


